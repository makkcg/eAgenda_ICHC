     */
    public function columns(): array
    {
        return [
            Column::add()
                ->title(trans('admin.id'))
                ->field('id', 'admins.id')
                ->makeInputRange(),

            Column::add()
                ->title(trans('admin.name'))
                ->field('name', 'admins.name')
                ->sortable()
                ->searchable()
                ->makeInputText(),

            Column::add()
                ->title(trans('admin.email'))
                ->field('email')
                ->sortable()
                ->searchable()
                ->makeInputText(),

            Column::add()
                ->title(trans('admin.role'))
                ->field('role', 'roles.name')
                ->sortable()
                ->searchable()
                ->makeInputText(),

            Column::add()
                ->title(trans('admin.phone_number'))
                ->field('phone_number')
                ->sortable()
                ->searchable()
                ->makeInputText(),

            Column::add()
                ->title('CREATED AT')
                ->field('created_at_formatted', 'created_at')
                ->searchable()
                ->sortable()
                ->makeInputDatePicker('created_at'),
        ];
    }

